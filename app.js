function renderFavToggle() {
  const count = DATA.products.filter(p => favourites[p.id]).length;
  els.favToggle.classList.toggle('active', state.favOnly);
  els.favToggle.setAttribute('aria-pressed', String(state.favOnly));
  els.favStar.textContent = state.favOnly ? '★' : '☆';
  els.favCount.textContent = ` (${count})`;
}

function renderList() {
  clearScanLock();
  const all = getProducts();
  const filtered = sortProducts(all.filter(matches));

  els.productList.innerHTML = '';
  els.emptyState.hidden = filtered.length !== 0;

  if (state.sort === 'category') {
    let lastCategory = null;
    filtered.forEach(p => {
      const catLabel = p.category || 'Uncategorised';
      if (catLabel !== lastCategory) {
        lastCategory = catLabel;
        const heading = document.createElement('div');
        heading.className = 'category-heading';
        heading.textContent = catLabel;
        els.productList.appendChild(heading);
      }
      els.productList.appendChild(buildCard(p));
    });
  } else {
    filtered.forEach(p => els.productList.appendChild(buildCard(p)));
  }
}

/* ---------------- Scan lock (long-press a QR to dim/blur the rest) ---------------- */

const SCAN_LOCK_MS = 500;
const SCAN_LOCK_MOVE_CANCEL_PX = 10;

let scanLockCard = null;
let scanLockTimer = null;
let scanLockPressStart = null;
let scanLockSuppressNextClick = false;

function clearScanLock() {
  if (scanLockCard) scanLockCard.classList.remove('scan-lock-target');
  els.productList.classList.remove('qr-locked');
  scanLockCard = null;
}

function armScanLock(card, point) {
  cancelScanLockPress();
  scanLockPressStart = { x: point.clientX, y: point.clientY };
  scanLockTimer = setTimeout(() => {
    clearScanLock(); // release whichever card was previously locked, if any
    scanLockCard = card;
    card.classList.add('scan-lock-target');
    els.productList.classList.add('qr-locked');
    scanLockSuppressNextClick = true;
    scanLockTimer = null;
  }, SCAN_LOCK_MS);
}
function cancelScanLockPress() {
  clearTimeout(scanLockTimer);
  scanLockTimer = null;
  scanLockPressStart = null;
}

function attachScanLock(card) {
  card.addEventListener('pointerdown', (e) => {
    if (e.button !== 0 && e.pointerType === 'mouse') return;
    armScanLock(card, e);
  });
  card.addEventListener('pointermove', (e) => {
    if (!scanLockTimer || !scanLockPressStart) return;
    const dx = e.clientX - scanLockPressStart.x;
    const dy = e.clientY - scanLockPressStart.y;
    if (Math.hypot(dx, dy) > SCAN_LOCK_MOVE_CANCEL_PX) cancelScanLockPress();
  });
  card.addEventListener('pointerup', cancelScanLockPress);
  card.addEventListener('pointerleave', cancelScanLockPress);
  card.addEventListener('pointercancel', cancelScanLockPress);
}

// runs before any card's own click handler (capture phase): while scan
// lock is active, every tap just releases it rather than doing its
// normal thing (open the modal, copy the SKU, etc.)
document.addEventListener('click', (e) => {
  if (scanLockSuppressNextClick) {
    // this is the release-click of the long-press gesture that just
    // armed the lock — swallow it without touching the lock we just set
    scanLockSuppressNextClick = false;
    e.preventDefault();
    e.stopPropagation();
    return;
  }
  if (scanLockCard) {
    e.preventDefault();
    e.stopPropagation();
    clearScanLock();
  }
}, true);

function buildCard(p) {
  const card = document.createElement('div');
  card.className = 'product-card' + (p.hidden ? ' card-hidden' : '');
  card.dataset.color = p.aisle;

  const img = document.createElement(p.sku ? 'img' : 'div');
  img.className = 'card-img' + (p.sku ? '' : ' placeholder');
  if (p.sku) {
    img.src = imageUrl(p.sku);
    img.alt = p.name;
    img.onerror = () => { img.replaceWith(placeholderIcon(p.aisle)); };
  } else {
    img.textContent = aisleIcon(p.aisle);
  }
  card.appendChild(img);

  const body = document.createElement('div');
  body.className = 'card-body';
  body.innerHTML = `
    <div class="card-name">${escapeHtml(p.name)}</div>
    <div class="card-meta">
      <button class="star-btn${p.favourite ? ' active' : ''}" aria-label="Toggle favourite" data-star>${p.favourite ? '★' : '☆'}</button>
      <span class="aisle-badge">${escapeHtml(p.aisle)}</span>
      <span class="card-category">${escapeHtml(p.category || 'Uncategorised')}</span>
      ${state.adminMode ? `<button class="hide-btn${p.hidden ? ' active' : ''}" aria-label="Toggle stocked at this store" data-hide>${p.hidden ? '🚫 Hidden here' : 'Hide here'}</button>` : ''}
    </div>
    ${p.sku ? `<span class="card-sku" data-sku="${escapeHtml(p.sku)}" title="Tap to copy">SKU ${escapeHtml(p.sku)}</span>` : ''}
  `;
  card.appendChild(body);
  body.querySelector('[data-star]').addEventListener('click', (e) => {
    e.stopPropagation();
    toggleFavourite(p.id);
  });
  const hideBtn = body.querySelector('[data-hide]');
  if (hideBtn) hideBtn.addEventListener('click', (e) => {
    e.stopPropagation();
    toggleHidden(p.id);
  });

  if (p.sku) {
    const qrWrap = document.createElement('div');
    qrWrap.className = 'card-qr';
    card.appendChild(qrWrap);
    // eslint-disable-next-line no-undef
    new QRCode(qrWrap, { text: p.sku, width: 80, height: 80, colorDark: '#1B211D', colorLight: '#ffffff' });
    attachScanLock(card);
  } else {
    const needsSku = document.createElement('div');
    needsSku.className = 'card-needs-sku';
    needsSku.textContent = 'Add SKU';
    card.appendChild(needsSku);
  }

  card.addEventListener('click', (e) => {
    if (e.target.closest('.card-sku')) {
      e.stopPropagation();
      copyToClipboard(e.target.closest('.card-sku').dataset.sku);
      return;
    }
    if (state.adminMode) {
      openAdminEdit(p.id);
    } else if (p.sku) {
      openQrModal(p.id);
    } else {
      openAdminEdit(p.id);
    }
  });

  return card;
}

function aisleIcon(aisle) { return ICONS[aisle] || DEFAULT_ICON; }
function placeholderIcon(aisle) {
  const d = document.createElement('div');
  d.className = 'card-img placeholder';
  d.textContent = aisleIcon(aisle);
  return d;
}
function escapeHtml(str) {
  const div = document.createElement('div');
  div.textContent = str;
  return div.innerHTML;
}

/* ---------------- Copy to clipboard ---------------- */

function copyToClipboard(text) {
  const done = () => showToast(`Copied SKU ${text}`);
  if (navigator.clipboard && window.isSecureContext) {
    navigator.clipboard.writeText(text).then(done).catch(() => fallbackCopy(text, done));
  } else {
    fallbackCopy(text, done);
  }
}
function fallbackCopy(text, done) {
  const ta = document.createElement('textarea');
  ta.value = text;
  ta.style.position = 'fixed';
  ta.style.opacity = '0';
  document.body.appendChild(ta);
  ta.select();
  try { document.execCommand('copy'); done(); } catch (e) { showToast('Could not copy'); }
  document.body.removeChild(ta);
}

